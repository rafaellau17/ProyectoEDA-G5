    private void iniciarAlertaPendientesTimer() {
        if (alertaTimer != null && alertaTimer.isRunning()) {
            alertaTimer.stop();
        }
        
        alertaTimer = new Timer(5000, e -> {
            if (!alertaMostrada) {
                ListaTramitesPendientes.actualizarTramitesPendientes();
                
                if (ListaTramitesPendientes.hayPendientes()) {
                    new AlertaPendientesDialog(this, true).setVisible(true);
                }
                
                alertaMostrada = true;
            }
            
            alertaTimer.stop();
        });
        
        alertaTimer.setRepeats(false);
        alertaTimer.start();
    }

    private void Poblar()
    {
        modelo.setRowCount(0);
        
       NodoDoble<DataExpediente> nodoAux = listaExpedientes.getCabeza();
       String[] datos = new String[6];
       
       for(int i = 0; i < listaExpedientes.longitud(); i++)
       {
           DataExpediente expAux = nodoAux.getItem();
           datos[0] = String.valueOf(expAux.getId());
           datos[1] = String.valueOf(expAux.getNombres());
           datos[2] = String.valueOf(expAux.getDni());
           datos[3] = String.valueOf(expAux.getTipo());
           datos[4] = String.valueOf(expAux.getPrioridad());
           datos[5] = String.valueOf(expAux.getContadorTramites());
           modelo.addRow(datos);
           nodoAux = nodoAux.getSgteNodo();
       }
    }    
    
    @Override
    public void setVisible(boolean visible) {
        super.setVisible(visible);
        if (visible) {
            alertaMostrada = false;
            iniciarAlertaPendientesTimer();
        } else {
            if (alertaTimer != null && alertaTimer.isRunning()) {
                alertaTimer.stop();
            }
        }
    }
    
